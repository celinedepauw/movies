<?php

class Movie
{
    private $id;
    private $name;
    private $year;
    private $duration;
    private $category;
    private $nationality;


    // récupération de tous les films pour une catégorie donnée
    public function findAllInCategory($idCategory)
    {
        $pdo = new PDO(
            'mysql:dbname=movies;host=localhost;charset=UTF8',
            'movies',
            'movies',
            [PDO::ATTR_ERRMODE => PDO::ERRMODE_WARNING],
        );

        $sql = "
            SELECT
                *
            FROM `movie`
            WHERE
                `category` = {$idCategory}
        ";

        $statement = $pdo->query($sql);
        $movies = $statement->fetchAll(PDO::FETCH_CLASS, static::class);
        return $movies;
    }

    /**
     * Get the value of id
     */ 
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set the value of id
     *
     * @return  self
     */ 
    public function setId($id)
    {
        $this->id = $id;

        return $this;
    }

    /**
     * Get the value of name
     */ 
    public function getName()
    {
        return $this->name;
    }

    /**
     * Set the value of name
     *
     * @return  self
     */ 
    public function setName($name)
    {
        $this->name = $name;

        return $this;
    }

    /**
     * Get the value of year
     */ 
    public function getYear()
    {
        return $this->year;
    }

    /**
     * Set the value of year
     *
     * @return  self
     */ 
    public function setYear($year)
    {
        $this->year = $year;

        return $this;
    }

    /**
     * Get the value of duration
     */ 
    public function getDuration()
    {
        return $this->duration;
    }

    /**
     * Set the value of duration
     *
     * @return  self
     */ 
    public function setDuration($duration)
    {
        $this->duration = $duration;

        return $this;
    }

    /**
     * Get the value of category
     */ 
    public function getCategory()
    {
        return $this->category;
    }

    /**
     * Set the value of category
     *
     * @return  self
     */ 
    public function setCategory($category)
    {
        $this->category = $category;

        return $this;
    }

    /**
     * Get the value of nationality
     */ 
    public function getNationality()
    {
        return $this->nationality;
    }

    /**
     * Set the value of nationality
     *
     * @return  self
     */ 
    public function setNationality($nationality)
    {
        $this->nationality = $nationality;

        return $this;
    }
}